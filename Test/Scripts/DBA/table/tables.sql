CREATE TABLE call_log
(
	log_id NUMBER PRIMARY KEY,
	name VARCHAR2(200),
	pnumber VARCHAR2(200),
	type VARCHAR2(200),
	call_date VARCHAR2(100) UNIQUE,
	d_call_date DATE,
	duration VARCHAR2(100),
	phone VARCHAR2(100),
	timestamp TIMESTAMP 
);
CREATE TABLE QUERY_LOG
(
	log_id NUMBER PRIMARY KEY,
	query VARCHAR2(4000),
	status VARCHAR2(20),
	timestamp TIMESTAMP 
);
